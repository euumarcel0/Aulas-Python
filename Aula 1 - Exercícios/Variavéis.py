# Números inteiros
numero_inteiro = 10
print("Número inteiro:", numero_inteiro, "Tipo:", type(numero_inteiro))

# Números de ponto flutuante
numero_ponto_flutuante = 3.14
print("Número de ponto flutuante:", numero_ponto_flutuante, "Tipo:", type(numero_ponto_flutuante))

# Caracteres (Strings de comprimento 1)
caractere = 'a'
print("Caractere:", caractere, "Tipo:", type(caractere))

# Booleanos
booleano = True
print("Booleano:", booleano, "Tipo:", type(booleano))

# Strings
texto = "Olá, mundo!"
print("String:", texto, "Tipo:", type(texto))